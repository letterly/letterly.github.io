function customize(){
    custom.style.display = "block"
    custom.value = ""
    for(h of Object.entries(keyboards.Osage)){
        custom.value += `${h[0]}:${h[1]}` + "\r"
    }
}

function customKeySet(){
    for(g of custom.value.split("\n")) document.getElementById(g.split(":")[0]).textContent = g.split(":")[1]
}